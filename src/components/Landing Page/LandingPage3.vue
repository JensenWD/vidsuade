  export default {
    name: "LandingPage3",
    methods: {
      scrollIntoView() {
        var element = document.getElementById("contact_form");
        element.scrollIntoView({behavior: "smooth", block: "start", inline: "nearest"});
      },
      formResponse() {
        $(document).ready(function () {
          $('#contact .row').each(function () {
            $(this).addClass('d-none');
          });

          $('#thanksResponse').removeClass('d-none');
        })
      },
      getUrlParams(prop) {
        var params = {};
        var search = decodeURIComponent(window.location.href.slice(window.location.href.indexOf('?') + 1));
        var definitions = search.split('&');

        definitions.forEach(function (val, key) {
          var parts = val.split('=', 2);
          params[parts[0]] = parts[1];
        });

        return (prop && prop in params) ? params[prop] : params;
      }
    },
    created() {
      if(this.getUrlParams('thanks') === 1)
        this.formResponse();
    },
    mounted() {

        let url = window.location.toString();

        if (url.includes('mobileapps')) {
            $('.sticky-top').css({'display': 'none'});
            //$('.footer_bg').css({'display': 'none'});
